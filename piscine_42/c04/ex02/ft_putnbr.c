#include <unistd.h>

void	ft_putnbr(int nb)
{
	if (nb < 0)
	{
		if (nb == (-2147483648))
			write (1, "-2147483648", 11);
		else
		{
			write(1, "-", 1);
			nb = nb * -1;
			ft_putnbr(nb);
		}
	}
	else if (nb < 10 && nb >= 0)
	{
		nb = nb + '0';
		write (1, &nb, 1);
	}
	else if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
}
